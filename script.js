    void subtitle.offsetWidth;
    subtitle.classList.add('slide-up');

    heroImg.src = images[currentPage];
    heroImg.classList.remove('zoom-in');
    void heroImg.offsetWidth;
    heroImg.classList.add('zoom-in');

